const month = 5;

switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("aplr");
        break;
    

    default:
        console.log("default case");
        break;
}

 
